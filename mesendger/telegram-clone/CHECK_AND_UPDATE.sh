#!/bin/bash
# Скрипт для проверки и обновления кода на сервере

cd /var/www/mesendger

echo "=========================================="
echo "ШАГ 1: Проверка текущего кода"
echo "=========================================="

# Проверяем, есть ли диагностика в файле
if grep -q "ДИАГНОСТИКА ФИЛЬТРАЦИИ" server/routes/activity.js; then
    echo "✅ Диагностика найдена в файле"
else
    echo "❌ Диагностика НЕ найдена - код не обновлен!"
    echo ""
    echo "Обновляем код..."
    sudo -u appuser git pull origin main
    echo ""
    echo "Проверяем снова..."
    if grep -q "ДИАГНОСТИКА ФИЛЬТРАЦИИ" server/routes/activity.js; then
        echo "✅ Диагностика найдена после обновления"
    else
        echo "❌ Диагностика все еще не найдена!"
        echo "Проверьте путь к файлу:"
        ls -la server/routes/activity.js
        exit 1
    fi
fi

echo ""
echo "=========================================="
echo "ШАГ 2: Показываем строки с диагностикой"
echo "=========================================="
grep -n "ДИАГНОСТИКА ФИЛЬТРАЦИИ\|Уникальных приложений\|Всего записей приложений" server/routes/activity.js | head -5

echo ""
echo "=========================================="
echo "ШАГ 3: Перезапускаем сервер"
echo "=========================================="
sudo -u appuser pm2 restart mesendger-server

echo ""
echo "=========================================="
echo "✅ ГОТОВО!"
echo "=========================================="
echo "Теперь:"
echo "1. Откройте детали пользователя в браузере"
echo "2. Выполните: pm2 logs mesendger-server --lines 100 | grep -A 25 'ДИАГНОСТИКА'"
echo "=========================================="

